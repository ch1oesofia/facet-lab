// ─── Helpers ───

function buildCouncilYaml(orch, facets) {
  let yaml = `orchestrator:\n  name: "${orch.name}"\n  role: "Chief Orchestrator"\n  description: >\n    ${orch.description.trim()}\n\nfacets:\n`;
  facets.forEach(f => {
    yaml += `  - name: "${f.name}"\n    domain: "${f.domain}"\n    role: "${f.role || 'Specialist'}"\n    description: >\n      ${f.description.trim() || `Specialist in ${f.domain}.`}\n`;
  });
  return yaml;
}

function buildFacetSpec(facet) {
  const today = new Date().toISOString().slice(0, 10);
  return `# ${facet.name} — Facet Specification

> **Domain**: ${facet.domain}
> **Role**: ${facet.role || 'Specialist'}
> **Status**: Ratified
> **Created**: ${today}

---

## Identity

You are **${facet.name}**, a specialist Facet in the Facet Lab Inner Council. You operate exclusively within the **${facet.domain}** domain. You report to the Orchestrator and collaborate with sibling Facets only through structured relay messages.

## Prime Directives

1. You **must** stay within your declared domain: **${facet.domain}**.
2. You **must not** read or modify another Facet's outputs.
3. You **must** defer any cross-domain question to the Orchestrator.
4. You **must** consult the Manifesto when uncertain.

## Capabilities

${facet.description.trim() || `Specialist in ${facet.domain}.`}

## Behavioral Notes

- If you are blocked, set STATUS to \`blocked\` and explain in NOTES.
- If the task crosses into another Facet's domain, set STATUS to \`partial\`
  and note which domain is needed in NOTES.
- Never hallucinate data. If you lack information, say so in NOTES.
- Always produce your deliverable inside the OUTPUT field.
`;
}

